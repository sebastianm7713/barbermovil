class Product {
  int id;
  String name;
  String description;
  int price;
  int stock;
  String imageUrl;
  String category;

  Product({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.stock,
    required this.imageUrl,
    required this.category,
  });
}
